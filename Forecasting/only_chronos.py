def compute_metrics(y_true, y_pred, q10=None, q90=None):
    y_true = np.asarray(y_true, dtype=float)
    y_pred = np.asarray(y_pred, dtype=float)

    mae = np.mean(np.abs(y_true - y_pred))
    rmse = math.sqrt(np.mean((y_true - y_pred) ** 2))

    nz = y_true != 0
    mape = np.mean(np.abs((y_true[nz] - y_pred[nz]) / y_true[nz])) * 100 if nz.any() else np.nan

    coverage = np.nan
    if q10 is not None and q90 is not None:
        q10 = np.asarray(q10, dtype=float)
        q90 = np.asarray(q90, dtype=float)
        coverage = np.mean((y_true >= q10) & (y_true <= q90)) * 100

    return {
        "MAE": mae,
        "RMSE": rmse,
        "MAPE": mape,
        "Coverage": coverage,
        "N": len(y_true)
    }

def make_past_ts(df_part):
    cols = ["item_id", "timestamp", "target"] + ALL_FEATURE_COLS
    return TimeSeriesDataFrame.from_data_frame(
        df_part[cols].copy(),
        id_column="item_id",
        timestamp_column="timestamp"
    )

def make_future_known(predictor_obj, past_data, block_test, item_id):
    future_template = predictor_obj.make_future_data_frame(past_data)
    future_template = future_template[future_template["item_id"] == item_id].copy()
    future_template = future_template.reset_index(drop=True)

    future_known = future_template.merge(
        block_test[["item_id", "timestamp"] + KNOWN_COVARIATES],
        on=["item_id", "timestamp"],
        how="left"
    )

    future_known = future_known.sort_values("timestamp").reset_index(drop=True)

    # Generate calendar covariates for possible extra rows in final block
    if "WeekNo" in KNOWN_COVARIATES:
        future_known["WeekNo"] = future_known["timestamp"].dt.isocalendar().week.astype(int)
    if "MonthNo" in KNOWN_COVARIATES:
        future_known["MonthNo"] = future_known["timestamp"].dt.month.astype(int)
    if "YearofWeek" in KNOWN_COVARIATES:
        future_known["YearofWeek"] = future_known["timestamp"].dt.year.astype(int)
    if "IsFirstWeek" in KNOWN_COVARIATES:
        future_known["IsFirstWeek"] = future_known["timestamp"].dt.day.le(7).astype(int)
    if "IsLastWeek" in KNOWN_COVARIATES:
        next_week = future_known["timestamp"] + pd.to_timedelta(7, unit="D")
        future_known["IsLastWeek"] = (future_known["timestamp"].dt.month != next_week.dt.month).astype(int)

    future_known[KNOWN_COVARIATES] = (
        future_known[KNOWN_COVARIATES]
        .ffill()
        .bfill()
        .fillna(0)
    )

    return TimeSeriesDataFrame.from_data_frame(
        future_known[["item_id", "timestamp"] + KNOWN_COVARIATES].copy(),
        id_column="item_id",
        timestamp_column="timestamp"
    )

def rolling_forecast_chronos(predictor_obj, best_model, output_prefix):
    history_df = pd.concat([train_df, val_df], ignore_index=True)
    history_df = history_df.sort_values(["item_id", "timestamp"]).reset_index(drop=True)

    test_df_sorted = test_df.sort_values(["item_id", "timestamp"]).reset_index(drop=True)

    all_predictions = []
    all_errors = []
    district_rows = []

    for item_id in common_ids:
        district_name = district_name_map.get(str(item_id), str(item_id))

        hist_item = history_df[history_df["item_id"] == item_id].copy().sort_values("timestamp").reset_index(drop=True)
        test_item = test_df_sorted[test_df_sorted["item_id"] == item_id].copy().sort_values("timestamp").reset_index(drop=True)

        if hist_item.empty or test_item.empty:
            continue

        rolling_hist = hist_item.copy()
        district_pred_parts = []

        start_idx = 0
        block_num = 1

        while start_idx < len(test_item):
            end_idx = min(start_idx + PREDICTION_LENGTH, len(test_item))
            block_test = test_item.iloc[start_idx:end_idx].copy().reset_index(drop=True)

            past_data = make_past_ts(rolling_hist)
            future_known_ts = make_future_known(predictor_obj, past_data, block_test, item_id)

            preds = predictor_obj.predict(
                data=past_data,
                known_covariates=future_known_ts,
                model=best_model
            )

            pred_df = preds.reset_index().copy()
            pred_df = pred_df[pred_df["item_id"] == item_id].sort_values("timestamp").reset_index(drop=True)

            pred_df = pred_df.rename(columns={
                "0.1": "pred_q10",
                "0.5": "pred_q50",
                "0.9": "pred_q90"
            })

            block_out = block_test[["item_id", "timestamp", "target"]].copy()
            block_out = block_out.merge(
                pred_df[["item_id", "timestamp", "pred_q10", "pred_q50", "pred_q90"]],
                on=["item_id", "timestamp"],
                how="left"
            )

            block_out["forecast_block"] = block_num
            district_pred_parts.append(block_out)

            # rolling update with actual observed values
            rolling_hist = pd.concat([rolling_hist, block_test], ignore_index=True)
            rolling_hist = rolling_hist.sort_values("timestamp").reset_index(drop=True)

            start_idx = end_idx
            block_num += 1

        district_pred = pd.concat(district_pred_parts, ignore_index=True)
        district_pred = district_pred.sort_values("timestamp").reset_index(drop=True)

        district_pred["DistrictName"] = district_name
        district_pred["step_index"] = np.arange(1, len(district_pred) + 1)

        district_pred["error"] = district_pred["target"] - district_pred["pred_q50"]
        district_pred["abs_error"] = district_pred["error"].abs()
        district_pred["squared_error"] = district_pred["error"] ** 2
        district_pred["ape_percent"] = np.where(
            district_pred["target"] == 0,
            np.nan,
            (district_pred["abs_error"] / district_pred["target"].abs()) * 100
        )
        district_pred["covered_by_q10_q90"] = (
            (district_pred["target"] >= district_pred["pred_q10"]) &
            (district_pred["target"] <= district_pred["pred_q90"])
        ).astype(int)

        all_predictions.append(district_pred.copy())

        all_errors.append(
            district_pred[
                [
                    "item_id", "DistrictName", "timestamp",
                    "forecast_block", "step_index",
                    "target", "pred_q10", "pred_q50", "pred_q90",
                    "error", "abs_error", "squared_error",
                    "ape_percent", "covered_by_q10_q90"
                ]
            ].copy()
        )

        m = compute_metrics(
            district_pred["target"].values,
            district_pred["pred_q50"].values,
            district_pred["pred_q10"].values,
            district_pred["pred_q90"].values
        )

        district_rows.append({
            "DistrictId": item_id,
            "DistrictName": district_name,
            "MAE": m["MAE"],
            "RMSE": m["RMSE"],
            "MAPE": m["MAPE"],
            "Coverage": m["Coverage"],
            "N": m["N"]
        })

    predictions_df = pd.concat(all_predictions, ignore_index=True)
    errors_df = pd.concat(all_errors, ignore_index=True)
    district_metrics_df = pd.DataFrame(district_rows).sort_values("DistrictName").reset_index(drop=True)

    overall = compute_metrics(
        errors_df["target"].values,
        errors_df["pred_q50"].values,
        errors_df["pred_q10"].values,
        errors_df["pred_q90"].values
    )

    overall_row = pd.DataFrame([{
        "DistrictId": "Overall",
        "DistrictName": "Overall",
        "MAE": overall["MAE"],
        "RMSE": overall["RMSE"],
        "MAPE": overall["MAPE"],
        "Coverage": overall["Coverage"],
        "N": overall["N"]
    }])

    final_metrics_df = pd.concat([district_metrics_df, overall_row], ignore_index=True)

    predictions_path = f"{output_prefix}_predictions.csv"
    errors_path = f"{output_prefix}_step_errors.csv"
    metrics_path = f"{output_prefix}_metrics.csv"

    predictions_df.to_csv(predictions_path, index=False)
    errors_df.to_csv(errors_path, index=False)
    final_metrics_df.to_csv(metrics_path, index=False)

    print("Predicted rows:", len(predictions_df))
    print("Expected test rows:", len(test_df_sorted))
    display(final_metrics_df)

    print("Saved:")
    print(predictions_path)
    print(errors_path)
    print(metrics_path)

    return predictions_df, errors_df, final_metrics_df

